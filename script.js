function filterPlayers() {
  let val = document.getElementById("senatorSearch").value;
  val = val.toLowerCase().split(" ").join("_");
  const players = document.querySelectorAll(`.player`);
  if (val === "") {
    players.forEach((player) => (player.style.opacity = 1));
  } else {
    const matchedEle = document.querySelectorAll(`[class*=${val}]`);
    players.forEach((player) => (player.style.opacity = 0.1));
    matchedEle.forEach((el) => (el.style.opacity = 1));
  }
}
